#include "libft.h"

void	*ft_memcpy(void *dest, void const *src, size_t len)
{
	char	*s;
	char	*d;
	size_t	i;

	s = (char *)src;
	d = (char *)dest;
	i = 0;
	if (dest == 0 && src == 0)
		return (dest);
	while (i < len)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
